<?php

function calSuma($valor1, $valor2){
    $resultado = $valor1 + $valor2;
    return $resultado;
}

function calResta($valor1, $valor2){
    $resultado = $valor1 - $valor2;
    return $resultado;
}

function calMultip($valor1, $valor2){
    $resultado = $valor1 * $valor2;
    return $resultado;
}
function calDiv($valor1, $valor2){
    $resultado = $valor1 / $valor2;
    return $resultado;
}

function calProducto($valor1, $valor2){
    $resultado = $valor1 % $valor2;
    return $resultado;
}

function calPotencia($valor1, $valor2){
    $resultado = $valor1 ** $valor2;
    return $resultado;
}

?>